<span id="salt"></span><br>
<br>
<div style="text-align: center"><b><a href="https://gbatemp.net/threads/saltfw-slim-alternative-firmware.423554/" class="internalLink"><span style="font-size: 22px"><span style="color: #0066cc">SaltFW</span></span></a></b>​</div><br>
<br>



<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		SaltFW
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">SaltFW is an A9LH/SysNAND and an extremely stripped down version of Luma3DS. Featuring all the patches of Luma3DS, but stripping away features like MenuHax support and EmuNAND/RedNAND support.<br>
<br>
<br>
<b>What can it do?</b><br>
<br>
<br>
<b>No configuration: </b>Much like ReiNAND, all patches are built directly in and on by default<br>
<br>
<br>
<b>Automatic Firmware patching: </b>There is nothing to download or patch, all the files you need are part of the launcher<br>
<br>
<br>
<b>Entrypoints: </b>A9LH is the only entrypoint for SaltFW. There is no support for loading SaltFW from MenuHax and MSET.<br>
<br>
<br>
<b>SysNAND only: </b>There is no support for EmuNAND/RedNAND.<br>
<br>
<br>
<b>Firm0/1 protection: </b>Firm0/1 protection is built directly into the launcher and is turned on by default.<br>
<br>
<br>
<b>No payload chainloading: </b>So you will need a bootloader for external applications like Decrypt9.<br>
<br>
<br>
<b>Reboot patches built into the launcher: </b>Like Luma3DS, these patches allow you to run games that require a lot of RAM like Sm4sh, Monster Hunter 4 and Pokemon: Sun & Moon. As well allows you to run GBA custom games, older DS(i) flashcarts with header, and launch custom DSi apps.<br>
<br>
<br>
<b>Region-Free: </b>Region-free for both CIA's and Game Carts.<br>
<br>
<br>
<b>Region/language patching: </b>Works like Luma3DS.<br>
<br>
<br>
<b>Boot splashes: </b>Allowing you to add a boot splash upon start up<br>
