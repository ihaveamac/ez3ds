	</div><div class="spoilerContainer" style="display:none">MenuHAX used to be the main source of auto-booting into your CFW and for the longest time was the only source. But in recent months a newcomer came to the scene known as "Arm9loaderhax" or "A9LH" for short. A9LH claim to fame was the fast boot, that booted seconds after pressing the power button, booting before sysNAND, and of course allowing CFW sysNAND. Now for the longest time A9LH was rather complicated to set up and came with the risks of bricking your 3DS. It also took over 4 hours to set up due to all of the downgrading, NAND backing up/restoring, ect. With this came some hesitation to update from MenuHAX to A9LH.<br>
