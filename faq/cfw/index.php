MenuHAX was a good entrypoint, but it's become grossly out of date when compared to A9LH. Currently the process of installing A9LH is fast, safe, stable, and more importantly up to date. If you follow <a href="https://3dshacks.xyz/guide/" target="_blank" class="externalLink" rel="nofollow">the guide</a>, you have nothing to loose and everything to gain.<br>
