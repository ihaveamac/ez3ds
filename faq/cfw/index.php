<span id="switch"></span><br>
<div style="text-align: center"><span style="font-size: 22px"><b>But is it safe to change my CFW? </b></span>​</div><br>



<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		I won't lose my games/saves/dlc will I?
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">This odd fear seems to be the biggest reason people continue to cling to CFWs like RXTools, despite knowing RXTools is dead. The simple truth is, your NANDs are safe when changing CFWs. CFWs don't actually write to the NANDs, they are only a means of reading them and mostly disabling signatures for Homebrews and "game backups." This means that no matter what CFW you go with, it won't have any effect on your actual NANDs. So you can safely switch from RXTools to any other CFW and retain the same NAND information. In fact, you can even multi-boot your system and boot into different CFWs, all running on the same NAND(s) without negative side effects. You can use this <a href="https://gbatemp.net/threads/tutorial-updating-from-rxtools-to-luma3ds.427735/" class="internalLink"><span style="color: #0066cc">guide</span></a> to updating to Luma3DS (or really any other CFW) on menuHAX. Or even use this <a href="https://3dshacks.xyz/guide/" target="_blank" class="externalLink" rel="nofollow"><span style="color: #0066cc">guide</span></a> for switching to A9LH. Regardless of the path you go on, just stop using RXTools, it's safe to stop using it and use something better.</div>
