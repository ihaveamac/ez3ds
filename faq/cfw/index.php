<b>Stable!</b><br>
<br>
These CFW's aren't as active as the Active CFWs listed on this CFW, but they are highly stable and usable. They tend to go long periods of time without much activity, but that doesn't mean the projects are dead, they just don't have any major bugs to fix. Major updates are added when they are needed for these CFW's, but they are the best CFW's for people who don't want overly frequent updates.<br>
