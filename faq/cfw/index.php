Compared to using MenuHax/A9LH, which autoboot into your EmuNAND (or protected SysNAND with A9LH,) leaving any vulnerable parts protected from accidental updates. It also takes up a lot less time, since it's just turn on and your CFW boots.<br>
<br>
Of course you obviously need other entrypoints for the initial CFW set up. But once you have your CFW set up, just set up an auto-boot using either A9LH/MenuHax, don't use any other entrypoints for daily CFW booting.</div>
