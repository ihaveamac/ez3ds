<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		Now why should you use Corbenik?
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">Corbenik is a lot like CakesFW, but where CakesFW is the Arch-Linux of CFW's, Corbenik is the Gentoo of CFW's. Corbenik is not for the faint of heart, it's a hardcore Dev/Power user oriented CFW. Corbenik is made for CFW users who want complete control over their system, giving them complete power to decide what patches they want to run and even give Dev power to the user. Corbenik is new the CFW world, but that doesn't mean it's unstable. Corbenik has had several releases and each release is heavily tested before it hits the public. The community is still small, but mostly made up of highly dedicated devs and power users who are willing to ensure each update is stable. Bugs are fixed almost as soon as they are found and patches are regularly added to improve the overall experience.<br>
<br>
Much like CakesFW, this one does not build its patches into the launcher. Which again means the same thing. Easier to set up for Bootloaders, but a little bit of clutter on the SD card. Of course the payoff is that you can see all the patches involved and have control over what patches you want to use.<br>
<br>
Corbenik actually does have its own updater to download the latest version directly to your 3DS<br>
<br>
<a href="https://gbatemp.net/threads/release-corbenik-cfw-updater-re.440297/" class="internalLink"><span style="color: #0066cc">Corbenik CFW Updater: RE</span></a><br>
<br>
Nightly build: <a href="https://github.com/chaoskagami/skeith" target="_blank" class="externalLink" rel="nofollow"><span style="color: #0066cc">skeith</span></a><br>
<br>
Corbenik may be new, but's it's very clear who this project is intended for. It's not for newbies just getting into the CFW's, it's for the devs/power users. Users who know what they are doing and know what they looking for. This is not a CFW that was made to hold your hand, it was made for those who want complete control over their system.</div>
</div><br>
