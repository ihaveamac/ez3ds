<span id="Corbenik"></span><br>
<br>
<div style="text-align: center"><b><a href="https://gbatemp.net/threads/corbenik-another-cfw-for-advanced-users-with-bytecode-patches.429612/" class="internalLink"><span style="font-size: 22px"><span style="color: #0066cc">Corbenik</span></span></a></b>​</div><br>
<br>



<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		Corbenik
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">Corbenik CFW is a CakesFW-inspired CFW, but not a fork of said CFW. It is a lot like CakesFW, being highly customisable, though unlike CakesFW it uses bytecode-based patches and a VM to run them (both in the CFW and Loader.) This CFW has quickly matured an extremely stable state since being released and is highly recommended for devs and power users alike. Corbenik does share some code with Luma3DS and CakesFW (and credits them too) but it is still based on its own completely independent code.<br>
<br>
<br>
<b>What can it do?</b><br>
<br>
<br>
<b>Advanced Menu to customise your CFW:</b> Much like CakesFW, you have an advanced menu to choose what options/loaders you want to use. Giving complete control over your CFW's functions.<br>
<br>
<br>
<b>A9LH support:</b> This CFW can both auto-boot from A9LH as well boot from a Boot Manager. Corbenik does not support MenuHax.<br>
<br>
<br>
<b>Firm0/1 Protection: </b>You have the option to turn off and on FIRM0/1 protection.<br>
<br>
<br>
<b>Supports SysNAND and EmuNAND: </b>You can boot into either a SysNAND CFW or EmuNAND CFW.<br>
<br>
<br>
<b>Multiple EmuNAND support:</b> You run up to 10 EmuNAND's on the same system!<br>
<br>
<br>
<b>Reboot/GBA/DS(i) patches:</b> You can use reboot patches to allow high memory/GBA/DS(i) games to boot on your old3DS. As well supports TWL patches, so you can launch DS(i) flashcarts with a header.<br>
<br>
<br>
<b>Region-Free patches:</b> You can launch games (both CIA and carts) from different regions.<br>
<br>
<br>
<b>Language emulation patches:</b> Allow you to emulate different languages on your system.<br>
<br>
<br>
<b>Blocks eShop, Cart, and NIM updates: </b>Basically protects your system from unwanted updates from Nintendo.<br>
<br>
<br>
<b>Friend code fix for older FWs:</b> Allows you to use older FW's and still access your Friend codes.<br>
<br>
<br>
<b>Dev mode support: </b>Turn your 3DS into a Dev 3DS!<br>
<br>
<br>
<b>New3DS CPU speed adjustments:</b> CPU speed hack is supported on N3DS. It gets automatically hidden on O3DS because it does nothing.<br>
<br>
<br>
<b>Payload Chainloading:</b> Corbenik supports A9LH payload chainloading, basically using its own menu to allow you to boot into A9LH apps. It's pretty easy to set up, just place the apps into the folder "/corbenik/boot" or /skeith/boot. The apps will then appear under “Chainloader” in the menu.<br>
<br>
<br>
<b>Nightly builds:</b> Corbenik provides nightly builds called "skeith" which can be downloaded directly from this link: <a href="https://github.com/chaoskagami/skeith/raw/master/rel/release.zip" target="_blank" class="externalLink" rel="nofollow"><span style="color: #0066cc">here</span></a><br>
<br>
Skeith can be install alongside your stable build allowing both Stable and Nightly to coexist without hassle. So you can test all of the new features without losing you stable build.<br>
<br>
<br>
<b>RO patching:</b> "RO is a service that loads .CRO files (CTR Relocatable Objects, maybe.) CRO files are like .DLLs (Windows) or Linux shared objects (.so). They have a separate signature check routine with RO - without a patch to RO to disable the signature check, RO will refuse to load unsigned or invalidly signed code from CROs. A large amount of code in some games (like Pokémon) is stored not in the ExeFS, but the CRO files." quoted from this <a href="https://gbatemp.net/threads/wip-faq-what-cfw-is-best-for-me.428509/page-7#post-6491242" class="internalLink"><span style="color: #0066cc">post</span></a><br>
<br>
<br>
<b>Bootloader themes:</b> You can install themes directly to your bootloader. Which both acts as a background for your bootloader and as a boot splash.<br>
<br>
<br>
<b>Bootloader screenshots:</b> By pressing "L+R+Start" you can take screenshots of your bootloader. They are saved under "/corbenik/var/cache/screenshot.ppm" or "/skeith/var/cache/screenshot.ppm"<br>
<br>
