Now why should people stop using MenuHAX? Because there's no reason to keep using it. Considering how much faster A9LH is to install and the benefits of having CFW on your sysNAND. This means you save space not having to use an emuNAND and more space not having to install your DSi/GBA games twice. You also get to launch payloads such as hourglass9, decrypt9, ect. all before the sysNAND even loads. This means that you can completely trash your sysNAND and still be able to restore it without the need for a hardmod. Adding to that, Nintendo can't do a targeted update to remove A9LH. The files needed to install A9LH are console specific, which means Nintendo would end up bricking the system and even unhacked systems if they tried to remove it. This is proven when when updating your system with a CFW without FIRM0/1 protection (Gateway,) which results in the system bricking. Of course all active CFWs currently support FIRM0/1 protection, thus bricking from an update shouldn't be a worry for any user.<br>
