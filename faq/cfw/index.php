<br>
Luma3DS has a large community around it, with a very active dev behind it. Almost all errors are addressed as soon as they are brought up. This has created an extremely beginner friendly CFW, that is extremely stable. You can expect regular updates and regular feedback, not only from the community, but also the dev herself. All the features mentioned above are built directly into the launcher, meaning there's very little clutter on your SD card and next to no space taken up. Just download, place on your SD card, and you are good to go, everything you need is in one spot. This makes setting up a breeze and so easy, anyone can do it! As well, all features you could ever need are just there. No patching this or downloading that, they are just there. You want to use A9LH with an EmuNAND? You can! You want to run it through MenuHax with EmuNAND? Go right ahead! CFW SysNAND through A9LH? Luma3DS was the first to do that! There are nightly builds for those who want the latest in the Luma3DS builds.<br>
