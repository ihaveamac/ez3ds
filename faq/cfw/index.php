<br>
<div style="text-align: center"><b><a href="https://gbatemp.net/threads/luma3ds-noob-proof-3ds-custom-firmware.411110/" class="internalLink"><span style="font-size: 22px"><span style="color: #0066cc">Luma3DS</span></span></a></b>​</div><br>



<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		Luma3DS
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">Luma3DS started out as a fork of ReiNAND created by Aurora Wright, but has since branched out to become its own entity. Luma3DS is currently one of the most widely used CFW's for the 3DS, mostly because of its extremely user-friendly approach and large community support for this project. But what does it offer and why should you use it?<br>
<br>
<br>
<b>What can it do?</b><br>
<br>
<br>
<b>User friendly menu:</b> Upon first boot this menu gives you a basic set up for the install as well can be accessed later for some basic configuration options.<br>
<br>
<br>
<b>A9LH, MenuHax, MSET support: </b>Meaning no matter what auto-boot entry point you are using, you will have support for it. It also supports the older MSET exploit.<br>
<br>
<b>Running Luma3DS from CTRNAND:</b> Luma3DS's arm9loaderhax can be copied to the CTRNAND. This allows booting the CFW directly from the CTRNAND. This also includes all the patches found in Luma3DS such as Reboot patches, FIRM protection, ect. Follow this <a href="https://plailect.github.io/Guide/updating-a9lh" target="_blank" class="externalLink" rel="nofollow">guide</a> to setting up Luma3DS onto your CTRNAND<br>
<br>
<b>EmuNAND and SysNAND support: </b>Luma3DS can run CFW on your sysNAND (using A9LH) and still supports emuNAND (all entrypoints)<br>
<br>
<br>
<b>Multi-EmuNAND support: </b>Allowing you to run multi-EmuNANDs on the SD card, because it can do that.<br>
<br>
<br>
<b>FIRM0/1 protection for A9LH: </b>This layer of protection allows you to always have your SysNAND up to date, without the risk of losing your CFW support and A9LH install. This feature is on by default.<br>
<br>
<br>
<b>Reboot patches for old3DS built into the launcher: </b>These patches allow you to run games that require a lot of RAM like Smash Bros. and Monster Hunter 4. As well allows you to run GBA custom games, older DS(i) flashcarts with header, and launch custom DSi apps.<br>
<br>
<br>
<b>Region Free:</b> You can play both physical games and CIA's from other regions.<br>
<br>
<br>
<b>Silent updates are disabled: </b>Updates will not download while in sleep mode<br>
<br>
<br>
(The next few are going to be quoted from the wiki because I don't know how to word them. They are the more technical stuff.)<br>
<br>
<b>Payload Chainloading:</b> This A9LH feature allows you to launch A9LH applications through Luma3DS's built-in boot loader. To quote the official wiki<br>
<br>
<br>
<b>Session persistency: Show current NAND in System Settings: </b>Quoted to better explain<br>
<br>
<br>
<br>
<b>New3DS CPU speed adjustments: </b>Which allows you to control your New3DS’ CPU clock speed<br>
<br>
<br>
<b>Boot splashes:</b> Allowing you to add a boot splash upon start up<br>
<br>
<br>
<b>Region/language emulation: </b>Just what is sounds like, you can spoof your system's region and language<br>
<br>
<br>
<b>Dev mode: </b>You can use Luma3DS to turn your system into a Dev 3DS (requires Dev build)<br>
<br>
<br>
<b>RO patching:</b> "RO is a service that loads .CRO files (CTR Relocatable Objects, maybe.) CRO files are like .DLLs (Windows) or Linux shared objects (.so). They have a separate signature check routine with RO - without a patch to RO to disable the signature check, RO will refuse to load unsigned or invalidly signed code from CROs. A large amount of code in some games (like Pokémon) is stored not in the ExeFS, but the CRO files." quoted from this <a href="https://gbatemp.net/threads/wip-faq-what-cfw-is-best-for-me.428509/page-7#post-6491242" class="internalLink"><span style="color: #0066cc">post</span></a><br>
<br>
<br>
<b>Security Pin:</b> Luma3DS provides the option to set up a button-combination before boot. This allows you to set up a PIN to protect your 3DS from unwanted tampering.<br>
<br>
<br>
<b>These are just the features I covered: The rest are in the </b><a href="https://github.com/AuroraWright/Luma3DS/wiki" target="_blank" class="externalLink" rel="nofollow"><b><span style="color: #0066cc">wiki</span></b></a></div>
</div><br>
<br>



<div class="bbCodeBlock bbCodeSpoiler">
