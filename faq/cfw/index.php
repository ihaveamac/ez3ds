<div style="text-align: center"><b><span style="font-size: 22px">Why shouldn't you use other Entrypoints?</span></b>​</div><br>
<br>



<div class="bbCodeBlock bbCodeSpoiler">
	<div class="type">
		Why only A9LH/MenuHax?
		<input type="button" class="button spoilerToggle" value="Show" onclick="toggleSpoiler(this)">	
	</div><div class="spoilerContainer" style="display:none">So why did I only cover 2 entrypoints in this thread? Well because others just aren't reliable nor really recommended for everyday use. Booting into your unprotected 9.0/9.2 SysNAND, then use it to force boot into your CFW through other entrypoints leaves your system unprotected and at risk of being accidentally updated and removing your hacks. It's also more time consuming to boot into your SysNAND, then boot up your entrypoint, then boot into your CFW.<br>
